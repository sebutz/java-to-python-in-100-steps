# HOW TO RETURN FROM A FUNCTION


def sum_of_two_numbers(number1, number2):
    sum = number1 + number2
    return sum


# return None
def sum_of_two_numbers_no_return(number1, number2):
    sum = number1 + number2


print(sum_of_two_numbers(10, 20))
print(sum_of_two_numbers_no_return(10, 20)) #None



